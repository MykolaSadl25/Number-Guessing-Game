import turtle

screen = turtle.Screen()
screen.title("Drawing Initials of my name with Turtle")
t = turtle.Turtle()
t.pensize(2)
t.pencolor("black")
t.shape("arrow")
t.penup()
t.right(180)
t.forward(400)
t.right(270)
t.forward(200)
t.pendown()
t.begin_fill()
t.fillcolor("blue")
t.right(180)
t.forward(400)
t.right(90)
t.forward(50)
t.right(45)
t.forward(200)
t.left(90)
t.forward(200)
t.right(45)
t.forward(50)
t.right(90)
t.forward(400)
t.right(90)
t.forward(50)
t.right(90)
t.forward(250)
t.left(135)
t.forward(200)
t.right(90)
t.forward(200)
t.left(135)
t.forward(250)
t.right(90)
t.forward(50)
t.end_fill()
t.penup()
t.hideturtle()
turtle.done()
